for (const [key, dict] of Object.entries(K)) {
  const missing = locales.filter((l) => !dict[l]);
  if (missing.length) throw new Error(`${key}: missing ${missing.join(",")}`);
}

for (const code of locales) {
  const f = `src/messages/${code}.json`;
  const m = JSON.parse(readFileSync(f, "utf8"));
  if (!m.feefreeApp || !m.driver) throw new Error(`${code}: namespaces missing`);
  for (const [key, dict] of Object.entries(K)) m.feefreeApp[key] = dict[code];
  // The two-door UI is gone; its wrong-door error key retires with it.
  delete m.driver.invalidDriverLogin;
  writeFileSync(f, JSON.stringify(m, null, 2) + "\n", "utf8");
  console.log(`${code} ✓`);
}
